package ss1_introduction_to_java.thuc_hanh.khai_bao_su_dung_bien;

public class Variable {
    public static void main(String[] args) {
        int i = 10; // khai báo biến i kiểu số nguyên int, có giá trị là 10
        float f = 20.5f; // khai báo biến f kiểu số thực float, có giá trị là 20.5
        double d = 20.5; // khai báo biến d kiểu số thực lớn double, có giá trị là 20.5
        boolean b = true; // khai báo biến b kiểu logic boolean, có giá trị là true
        char c = 'a'; // khai báo biến c kiểu ký tự char, có giá trị là 'a'
        String s = "Hà Nội"; // khai báo biến s kiểu chuỗi String, có giá trị là "Hà Nội"

        System.out.println("i= " + i);
        System.out.println("f= " + f);
        System.out.println("d= " + d);
        System.out.println("b= " + b);
        System.out.println("c= " + c);
        System.out.println("s= " + s);
    }
}
